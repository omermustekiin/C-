
#include <iostream>
using namespace std;
int main()

{
	cout << "değişken tipleri" << endl;
	//char değişken tipi

	char b = 'f';

	char s = 's';

	cout << b << endl;
	cout << s << endl;
	//integer (tamsay) değişken tipi

	int a = 50;

	int t;
	t = 60;

	cout << a << endl;
	cout << t << endl;

	//float-double-long double (ondalık sayılar için) değişken tipi
	double pi = 333.14;

	double c = 21.5;

	long double x = 17.5132;

	cout << pi << endl;
	cout << c << endl;
	cout << x << endl;

	//boolean (true-false -- doğru-yanlış) değişken tipi
	bool yil = true;

	bool deger = false;

	cout << yil << endl;
	cout << deger << endl;

}

